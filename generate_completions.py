import sys
import os
import re
import shtab

# Add src to path
sys.path.insert(0, os.path.abspath("src"))

from git_tools.commit_stats import get_parser as get_commit_parser
from git_tools.pack_stats import get_parser as get_pack_parser

def get_zsh_body(script):
    # Extract the main body of the shtab-generated zsh script
    # Everything from the first _shtab_ up to the typeset -A opt_args line
    match = re.search(r'(_shtab_.*)\ntypeset -A opt_args', script, re.DOTALL)
    if match:
        return match.group(1).strip()
    return script.strip()

def generate():
    # Git commit stats
    cp = get_commit_parser()
    cp.prog = "git-commit-stats"
    # Find the 'repo' argument (dest='repo')
    for action in cp._actions:
        if action.dest == 'repo':
            action.complete = shtab.DIR

    # Git pack stats
    pp = get_pack_parser()
    pp.prog = "git-pack-stats"
    # Find the 'repo' argument (dest='repo')
    for action in pp._actions:
        if action.dest == 'repo':
            action.complete = shtab.DIR

    # Bash
    commit_bash = shtab.complete(cp, shell="bash")
    pack_bash = shtab.complete(pp, shell="bash")

    def get_bash_body(script):
        return script.replace("# AUTOMATICALLY GENERATED by `shtab`", "").strip()

    with open("completions/git-tools.bash", "w") as f:
        f.write("# Bash completion for git-tools\n\n")
        f.write("# AUTOMATICALLY GENERATED by `shtab`\n\n")
        f.write(get_bash_body(commit_bash))
        f.write("\n\n")
        f.write(get_bash_body(pack_bash))
        f.write("\n")

    # Zsh
    commit_zsh = shtab.complete(cp, shell="zsh")
    pack_zsh = shtab.complete(pp, shell="zsh")

    with open("completions/git-tools.zsh", "w") as f:
        f.write("#compdef git-commit-stats git-pack-stats\n\n")
        f.write("# AUTOMATICALLY GENERATED by `shtab` via generate_completions.py\n\n")
        f.write("typeset -A opt_args\n\n")
        f.write(get_zsh_body(commit_zsh))
        f.write("\n\n")
        f.write(get_zsh_body(pack_zsh))
        f.write("\n\n")
        f.write("# Registration\n")
        f.write("if [[ $zsh_eval_context[-1] == eval ]]; then\n")
        f.write("  # When sourced/evaled, register both\n")
        f.write("  compdef _shtab_git_commit_stats git-commit-stats\n")
        f.write("  compdef _shtab_git_pack_stats git-pack-stats\n")
        f.write("else\n")
        f.write("  # When autoloaded, dispatch based on $service\n")
        f.write("  case $service in\n")
        f.write("    git-commit-stats) _shtab_git_commit_stats \"$@\" ;;\n")
        f.write("    git-pack-stats) _shtab_git_pack_stats \"$@\" ;;\n")
        f.write("    *) # Fallback for other cases\n")
        f.write("      if [[ \"$words[1]\" == \"git-pack-stats\" ]]; then\n")
        f.write("        _shtab_git_pack_stats \"$@\"\n")
        f.write("      else\n")
        f.write("        _shtab_git_commit_stats \"$@\"\n")
        f.write("      fi\n")
        f.write("      ;;\n")
        f.write("  esac\n")
        f.write("fi\n")

if __name__ == "__main__":
    generate()
